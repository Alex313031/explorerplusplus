#include "pch.h"
#include "MessageLoopTestHelper.h"

void PumpMessageLoopUntilIdle()
{
	MSG msg;

	while (PeekMessage(&msg, nullptr, 0, 0, PM_REMOVE))
	{
		TranslateMessage(&msg);
		DispatchMessage(&msg);
	}
}
